    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String description = jTextField1.getText();
        String fee;
        try {
            Double.parseDouble(jTextField2.getText());
            fee = jTextField2.getText();
        } catch (Exception e) {
            fee = "0.00";
        }

        if (description.isBlank()) {
            JOptionPane.showMessageDialog(this, "Enter Subject Description", "Warning", JOptionPane.WARNING_MESSAGE);
            jTable1.grabFocus();
        } else if (fee.isBlank()) {
            JOptionPane.showMessageDialog(this, "Enter Subject Fee", "Warning", JOptionPane.WARNING_MESSAGE);
            jTextField2.grabFocus();
        } else if (Pattern.compile("0.0|0|[0-9][.][0]*").matcher(fee).matches()) {
            JOptionPane.showMessageDialog(this, "invalid Fee", "Warning", JOptionPane.WARNING_MESSAGE);
            jTextField2.grabFocus();
        } else {

            String id = "SUB : " + String.valueOf(System.currentTimeMillis());

            try {
                ResultSet rrs = MySQL.s("SELECT * FROM `subject` WHERE `subject`.`Description`='" + description + "' AND `status`='1' ;");

                if (rrs.next()) {
                    JOptionPane.showMessageDialog(this, "Subject Alredy Exsist", "Warning", JOptionPane.WARNING_MESSAGE);
                    clearFileds();
                } else {
                    MySQL.iud("INSERT INTO `subject` (`subno`,`description`,`price`) VALUES('" + id + "','" + description + "','" + fee + "') ");

                    clearFileds();

                    JOptionPane.showMessageDialog(this, "Subject Added", "Sucess", JOptionPane.INFORMATION_MESSAGE);

                    loadSubjects();
                }
            } catch (Exception ex) {
                Logger.getLogger(Subject.class.getName()).log(Level.SEVERE, null, ex);
            }

        }


    }//GEN-LAST:event_jButton1ActionPerformed

