    private void loadSubjects() {
        try {
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            ResultSet rs = MySQL.s("SELECT * FROM `subject` WHERE `status`='1'");

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString(1));
                v.add(rs.getString(2));
                v.add(rs.getString(3));
                v.add("Delete");
                dtm.addRow(v);
            }
            if (this.c != null) {
                this.c.loadSubjects();
            }

        } catch (Exception e) {

        }

    }

